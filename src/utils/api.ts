/**
 * =============================================================================
 *  ФАЙЛ: src/utils/api.ts
 * =============================================================================
 *
 *  1. НАЗНАЧЕНИЕ
 *     ----------
 *     Единственная утилита для HTTP-запросов к PokeAPI (Pokémon REST API).
 *     Проксирует все вызовы через серверный endpoint `/api/pokeapi/*`, чтобы
 *     обойти CORS-ограничения браузера и скрыть реальный base URL от клиента.
 *
 *  2. ЗАВИСИМОСТИ
 *     ------------
 *     - Node.js/браузерный `fetch` (глобально, без импорта)
 *     - Серверный роут `/api/pokeapi/:path` — прокси к https://pokeapi.co/api/v2/
 *       (определён в серверной части приложения)
 *     - Никакие сторонние пакеты не требуются; никакого глобального состояния
 *       (кэш, store) не используется — каждый вызов идёт напрямую к API.
 *
 *  3. ГДЕ ИСПОЛЬЗУЕТСЯ
 *     -----------------
 *     Импортируется из '../utils/api.js' (или '../utils/api') в:
 *       - src/ui/tm.ts             — загрузка данных покемона для TM-совместимости
 *       - src/ui/pokedex.ts        — загрузка видов покемонов для списка
 *       - src/ui/levelup_moves.ts  — загрузка данных покемона для анимации
 *       - src/ui/inventory.ts      — загрузка данных покемона для инвентаря
 *       - src/ui/evolution.ts      — эволюционные цепочки и спрайты
 *       - src/battle/core.ts       — боевой движок (активный покемон, виды, атаки)
 *
 *  4. ЭКСПОРТЫ
 *     ---------
 *     Имя                 | Описание
 *     --------------------|-----------------------------------------------
 *     fetchPokeAPI        | Асинхронный GET-запрос к PokeAPI через прокси.
 *                         | Принимает путь или полный URL, возвращает JSON.
 *                         | Нигде не кэшируется — каждый вызов отправляет
 *                         | новый HTTP-запрос.
 *
 * =============================================================================
 */

/**
 * fetchPokeAPI(pathOrUrl)
 *
 * Выполняет GET-запрос к PokeAPI через серверный прокси-роут `/api/pokeapi/`.
 * Если передан полный URL вида `https://pokeapi.co/api/v2/...`, префикс
 * автоматически отрезается, остаётся только относительный путь.
 *
 * Вход:
 *   - pathOrUrl (string):
 *       - Относительный путь: `"pokemon/25"` → запрос к `/api/pokeapi/pokemon/25`
 *       - Полный URL: `"https://pokeapi.co/api/v2/pokemon-species/6"` → префикс
 *         `https://pokeapi.co/api/v2/` отбрасывается, запрос к `/api/pokeapi/pokemon-species/6`
 *
 * Выход:
 *   - Promise<object> — распарсенный JSON-ответ от PokeAPI.
 *
 * Ошибки:
 *   - Если HTTP-статус не 2xx, бросает Error с текстом `"PokeAPI error: {status}"`.
 *     Вызывающий код отвечает за обработку (try/catch).
 */
export async function fetchPokeAPI(pathOrUrl) {
  const path = pathOrUrl.replace('https://pokeapi.co/api/v2/', '');
  const res = await fetch(`/api/pokeapi/${path}`);
  if (!res.ok) throw new Error(`PokeAPI error: ${res.status}`);
  return res.json();
}
